        /*
         * MAP LEGEND
         * 0=grass
         * 1=rocks
         * 2=bushes
         * 3=spawn points
        */
        
        // Determining MAX row and MAX col (should be 50x50)
        static int maxMapRow = mapOutline.length;
        static int maxMapCol = mapOutline[0].length;

        // Array of spawns
        int[][] spawnPoint = new int[11][2];

        Random random = new Random();

        /**
         * Determines where the spawns are on the map and adds them to the spawnPoint
         * list
         */
        void getSpawns() {
                int x = 0;
                for (int i = 0; i < maxMapRow; i++) {
                        for (int j = 0; j < maxMapCol; j++) {

                                if (mapOutline[i][j] == 3) {
                                        spawnPoint[x][0] = j * TILE_SIZE;
                                        spawnPoint[x][1] = i * TILE_SIZE;
                                        x++;
                                }

                        }
                }
        }

        /**
         * Sets the spawn for the player (randomly)
         * The rest of the spawns are used to create new enemies (10 total)
         */
        void setSpawns() {
                // Creating arraylist of integers and filing them from 0-10
                ArrayList<Integer> listNums = new ArrayList<>();
                for (int i = 0; i < 11; i++) {
                        listNums.add(i);
                }

                // Generating random number from 0-11 and setting player spawn
                int x = random.nextInt(11);
                ForkKnife.player.worldXPos = spawnPoint[x][0];
                ForkKnife.player.worldYPos = spawnPoint[x][1];

                // Removing that number from the list (NOTE: index = object)
                listNums.remove(x);

                // Generating new enemies based on each spawn
                for (Integer num : listNums) {
                        ForkKnife.enemies.add(new Enemy(spawnPoint[num][0], spawnPoint[num][1]));
                }
        }

        /**
         * Finds all obstacles on map, and adds them to obstacle list
         */
        void getObstacles() {
                for (int i = 0; i < maxMapRow; i++) {
                        for (int j = 0; j < maxMapCol; j++) {

                                int tileWorldXPos = i * TILE_SIZE;
                                int tileWorldYPos = j * TILE_SIZE;

                                if (mapOutline[i][j] == 1 || mapOutline[i][j] == 2) {
                                        ForkKnife.obstaclesForCollision.add(new Rectangle(tileWorldXPos, tileWorldYPos, TILE_SIZE, TILE_SIZE));
                                }

                        }
                }
        }
        /**
        * Drawing the map
        * @param gc: graphics console to draw the map on
        */
        void drawMap(GraphicsConsole gc) {
                synchronized (gc) {
                        for (int i = 0; i < maxMapRow; i++) {
                                for (int j = 0; j < maxMapCol; j++) {

                                        int tileWorldXPos = i * TILE_SIZE;
                                        int tileWorldYPos = j * TILE_SIZE;

                                        int tileScreenXPos = tileWorldXPos - ForkKnife.player.worldXPos
                                                        + ForkKnife.player.screenXPos;
                                        int tileScreenYPos = tileWorldYPos - ForkKnife.player.worldYPos
                                                        + ForkKnife.player.screenYPos;
                                        
                                        // Determining if the tile should appear on the screen. If so, then draw it [optimization]
                                        if(tileWorldXPos + TILE_SIZE > ForkKnife.player.worldXPos - ForkKnife.player.screenXPos &&
                                                tileWorldXPos - TILE_SIZE < ForkKnife.player.worldXPos + ForkKnife.player.screenXPos &&
                                                tileWorldYPos + TILE_SIZE > ForkKnife.player.worldYPos - ForkKnife.player.screenYPos &&
                                                tileWorldYPos - TILE_SIZE < ForkKnife.player.worldYPos + ForkKnife.player.screenYPos) {

                                                gc.drawImage(ForkKnife.grass, tileScreenXPos, tileScreenYPos);
                                                gc.setColor(new Color(24,70,50));
                                                gc.setStroke(3);
                                                gc.drawRect(tileScreenXPos, tileScreenYPos, TILE_SIZE, TILE_SIZE);

                                                if (mapOutline[i][j] == 1) {
                                                        gc.drawImage(ForkKnife.rock, tileScreenXPos, tileScreenYPos);
                                                }
                                                if (mapOutline[i][j] == 2) {
                                                        gc.drawImage(ForkKnife.bush, tileScreenXPos, tileScreenYPos);
                                                }

                                        }

                                }
                        }
                }

        }

